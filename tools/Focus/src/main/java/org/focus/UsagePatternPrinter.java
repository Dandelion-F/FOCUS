	private static final Logger log = LogManager.getFormatterLogger(UsagePatternPrinter.class);

	public UsagePatternPrinter(String srcDir, String subFolder, int testingStartPos, int testingEndPos) {
		this.srcDir = srcDir;
		this.subFolder = subFolder;
		this.recDir = this.srcDir + this.subFolder + "/" + "Recommendations" + "/";
		this.tiDir = this.srcDir + this.subFolder + "/" + "TestingInvocations" + "/";
		this.testingStartPos = testingStartPos;
		this.testingEndPos = testingEndPos;
		this.eval = createRascalEvaluator();
	}

	public void print() {
		Map<Integer, String> testingProjectsID = reader.readProjectList(srcDir + "List.txt", testingStartPos,
				testingEndPos);

		for (String project : testingProjectsID.values()) {
			// Get the top-ranked recommendation
			Set<String> topRecs = reader.readRecommendationFile(this.recDir + project, 1);
			String topRec = topRecs.iterator().next();
			String projectName = project.substring(0, project.length() - 4);
			String friendlyName = dataset.getOrDefault(projectName, "unknown");

			// The declaration it is meant for
			Map<String, String> testingInvocations = reader.getTestingInvocations2(tiDir + project);
			String decl = testingInvocations.keySet().iterator().next();

			log.info("Project:        %s [%s]", project, friendlyName);
			log.info("Declaration:    %s", decl);
			log.info("Recommendation: %s", topRec);

			// Print snippet of the recommended invocation
			// TODO: Where to retrieve the declaration for which the invocation is
			// recommended?

			String s = ((IString) eval.call("retrieveSnippet", vf.string(decl), vf.string(projectName))).getValue();

		}
	}

	private Evaluator createRascalEvaluator() {
		GlobalEnvironment heap = new GlobalEnvironment();
		ModuleEnvironment module = new ModuleEnvironment("$focus$", heap);
		PrintWriter stderr = new PrintWriter(System.err);
		PrintWriter stdout = new PrintWriter(System.out);
		Evaluator eval = new Evaluator(vf, stderr, stdout, module, heap);

		eval.addRascalSearchPathContributor(StandardLibraryContributor.getInstance());

		try {
			// FIXME: Hardcoded
			eval.addRascalSearchPathContributor(new URIContributor(URIUtil
					.createFileLocation("/home/dig/repositories/FOCUS-master/tools/FocusRascal/src")));
		} catch (URISyntaxException e) {
			log.error(e.getMessage(), e);
		}

		// Import the module we're interested in once
		eval.doImport(null, "focus::m3::Snippets");

		return eval;
	}

